		  		<h4>Renal Tubular Diseases</h4>
		  		<p>Renal tubular function governs reabsorption of water and solutes from the glomerular  ltrate. In renal tubulopathies the primary genetic defect causes loss of function of a speci c renal transport protein or signalling molecule. The following are classi ed under genetic renal tubular disorders</p>
		  		<ul class="regularListing">
		  			<li>ADPKD</li>
