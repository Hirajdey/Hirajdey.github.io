            <p>Whole Exome Sequencing (WES), also known as Targeted exome capture is sequencing the protein coding sequences of genome (exomes) rather than the complete genome. It is estimated that the protein coding regions of the human genome constitute about 85% of the disease-causing mutations. </p>
            <p>Exome sequencing is done to identify the functional variation that is responsible for both Mendelian and common diseases. Most of the diseases or phenotypes are caused by variations in coding regions, and so it is an alternative for Whole Genome Sequencing based on a research question. </p>
             <p>MedGenome has state-of-the-art Next Generation sequencing Illumina platforms Miseq, Hiseq 4000 / 2500 and X10 at its facilities in Foster City, California and Bangalore, India.</p>
                       
