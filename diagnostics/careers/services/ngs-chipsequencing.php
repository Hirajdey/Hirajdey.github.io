            <p>ChIP-seq is a powerful method to map transcription factor binding sites and histone modification status on a genome-wide scale. This technique is a combination of chromatin immunoprecipitation assays with sequencing. Main steps in the ChIP-seq include cross-linking a protein to chromatin, shearing the chromatin, using a specific antibody to precipitate the protein of interest with its associated DNA, reverse cross linking and finally purifying the associated DNA fragments.</p>
                        
