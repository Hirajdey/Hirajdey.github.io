            <p>RNA Sequencing is a powerful technique that allows researchers to study the complete transcriptome profiling. Transcriptome is complete set of transcripts, including their quantities available in the cell, for specific physiological condition.  Complete understanding of transcriptome gives enormous information on functional components of genome, molecular components of cell, tissues and knowledge on disease development. </p>
            <p>Different technologies such as Hybridization-based, sequence-based approaches and specialised microarrays have also been used earlier to study transcriptome, but they were found to be not so effective for large volumes of data. Development of RNA-Seq based on the high-throughput DNA sequencing method has solved that problem to a great extent.</p>
                       
