			<p class="nametitle">Introduction</p>
			<p>MedGenome Institutional Ethics Committee (MG-IEC) is an 

<strong>independent body</strong> constituted under the authority of the chairman with 

the primary goal of safeguarding the rights and wellbeing of research participants. The 

chairperson of MG-IEC is appointed from a different institution and the appointment of 

IEC members is solely based on their competence, experience and integrity.</p>
            
           <p class="nametitle">Responsibilities of MG-IEC </p>
			<p>The MG-IEC will review all types of research proposals 

involving human participants with a view to safeguard the <strong>dignity, rights, 

safety and wellbeing of all actual and potential research participants</strong> before 

approving the research proposals. The goals of research, however important, should 

never be permitted to override the health and wellbeing of the research subjects.</p>
            
            
            <p>The MG-IEC will ascertain whether all the cardinal principles of 

research ethics viz., <strong>Autonomy, Beneficence, Non-maleficence, Respect for Free 

and Informed Consent, Respect for Human Dignity, Respect for Vulnerable Persons, 

Respect for Privacy and Confidentiality and Justice</strong> are taken care of in 

planning, conducting and reporting of the proposed research. For this purpose, it will 

look into the aspects of protocol review, selection of participants, voluntary 

participation of potential participants, informed consent process, risk benefit ratio, 

distribution of burden and benefit, maintenance of privacy and confidentiality and 

provisions for appropriate compensations. It will review the proposals before the 

commencement of the study as well as review periodically until the completion of the 

study through appropriate well-documented procedures.</p>
            
            
            
