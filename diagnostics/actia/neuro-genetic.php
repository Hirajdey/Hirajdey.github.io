                                <h3>Neuro Cutaneous Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Neurological disorders that have cutaneous manifestations</span></li>
                                    <li><span>Are lifelong conditions that can cause tumors to grow inside the brain, spinal cord, organs, skin, and bones</span></li>
                                    <li><span>Example: Tuberous sclerosis (TS); Neurofibromatosis (NF)</span></li>
                                </ul>
                                <h3>Neuro Muscular Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Disorders that affect the peripheral nervous system</span></li>
                                    <li><span>Principle effect is on the ability to perform voluntary movements</span></li>
                                    <li><span>Include some of the most devastating disorders that afflict mankind</span></li>
                                    <li><span>Example: Limb Girdle Muscular Dystrophy; Spinal Muscular Atrophy; Duchenne Muscular Dystrophy</span></li>
                                </ul>
                                <h3>Neuro Degenerative Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Involves the progressive loss of structure or function of neurons, including death of neurons</span></li>
                                    <li><span>Can be serious or life-threatening</span></li>
                                    <li><span>Example: Adrenoleukodystrophy; Canavan disease</span></li>
                                </ul>
                                <h3>Neuro Metabolic Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Neurological manifestations are the prominent signs and symptoms of in born errors of metabolism</span></li>
                                    <li><span>Characterized by a lack or dysfunction of an enzyme or vitamin necessary for a specific chemical reaction in the body</span></li>
                                    <li><span>Mostly are presented in newborns and infants</span></li>
                                    <li><span>Example: Maple Syrup Urine Disease, Neuronal ceroid lipofuscinosis, Krabbe disease</span></li>
                                </ul>
                                <h3>Movement Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Characterised by excess or a paucity of movement that is not connected to weakness, paralysis or spasticity of the muscles</span></li>
                                    <li><span>Affect the speed, fluency, quality and ease of movements</span></li>
                                    <li><span>Often associated with poor coordination of hands, speech, and eye movements</span></li>
                                    <li><span>Clinical presentation is complex, often variable, and sometimes even bizarre</span></li>
                                    <li><span>Example: Spinocerebellar ataxia; Tourette’s syndrome; Juvenile Parkinsonism</span></li>
                                </ul>
                                <h3>Mitochondrial Disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Clinically heterogeneous group of genetic disorders arising due to mutations in mitochondrial DNA, or nuclear DNA coding for                               mitochondrial components</span></li>
                                    <li><span>Difficult to diagnose, because it affects each individual differently</span></li>
                                    <li><span>Symptoms can include seizures, strokes, severe developmental delays, inability to walk, talk, see, and digest food combined with a  host of other complications</span></li>
                                    <li><span>Example: Mitochondrial myopathy; Myoclonic Epilepsy with Ragged Red Fibers (MERRF)</span></li>
                                </ul>
