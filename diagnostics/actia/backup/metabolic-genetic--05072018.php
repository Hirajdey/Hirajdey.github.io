                        <div class="content-block genetics-of-eye-disorders">
                            <div class="text-content">
                                <h3 class="gray italic">The major categories of IEMs are:</h3>
                                <div class="spacer-20"></div>
                                <h3 class="bulleted ordered">Aminoacidopathies</h3>
                                <ul class="green-bullet bb">
                                    <li><span>A very heterogeneous group of disorders</span></li>
                                    <li><span>Eg: Phenylketonuria, hereditary tyrosinemia, non-ketotic hyperglycinemia, maple syrup urine disease and homocystinuria</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Organic acidemias</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Caused by abnormal metabolism of proteins, fats or carbohydrates</span></li>
                                    <li><span>Are characterized by marked metabolic acidosis with ketosis, often with elevated lactate and mild to moderate hyperammonemia</span></li>
                                    <li><span>Eg: methylmalonic or propionic acidemia, multiple carboxylase deficiency</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Fatty acid oxidation defects</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Also known as Beta-oxidation defects</span></li>
                                    <li><span>Characterized by hypoketotic hypoglycemia, hyperammonemia, and cardiomyopathy</span></li>
                                    <li><span>Medium-chain acyl-CoA dehydrogenase deficiency (MCAD) is among the most common of all IEMs and may account for 5% of SIDS 	    cases</span></li>
                                    <li><span>Eg: short, medium, and long- chain acyl-CoA dehydrogenase deficiency</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Primary lactic acidosis</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Present with severe lactic acidosis</span></li>
                                    <li><span>Eg: Pyruvate dehydrogenase, pyruvate carboxylase and cytochrome oxidase deficiencies</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Urea cycle defects</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Results from the inability to detoxify nitrogen</span></li>
                                    <li><span>Characterized by severe hyperammonemia and respiratory alkalosis, with a typical onset after 24 hours of age</span></li>
                                    <li><span>Eg: citrullinemia, ornithine transcarbamylase deficiency, and arginosuccinic aciduria</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Disorders of carbohydrate</h3>
                                <ul class="green-bullet bb">
                                    <li><span>A heterogeneous group caused by inability to metabolize specific sugars, aberrant glycogen synthesis, or disorders of gluconeogenesis</span></li>
                                    <li><span>May manifest with hypoglycemia, hepatosplenomegaly, lactic acidosis or ketosis</span></li>
                                    <li><span>Eg: galactosemia, hereditary fructose intolerance, fructose 1,6-diphosphatase deficiency and the glycogen storage diseases</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Lysosomal storage disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Caused by accumulation of glycoproteins, glycolipids, or glycosaminoglycans</span></li>
                                    <li><span>Usually present later in infancy, not with a specific laboratory abnormality, but with organomegaly, facial coarseness and neurodegeneration and show a progressively degenerative course</span></li>
                                    <li><span>Eg: mucopolysaccharidosis, Tay-Sachs, Niemann-Pick disease, Gaucher’s</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Peroxisomal disorders</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Results from failure of the peroxisomal enzymes</span></li>
                                    <li><span>May present with features similar to the lysosomal storage disorders</span></li>
                                    <li><span>Eg: Zellweger syndrome and neonatal adrenoleukodystrophy</span></li>
                                </ul>

                                <h3 class="bulleted ordered">Others</h3>
                                <ul class="green-bullet bb">
                                    <li><span>Disordered steroidogenesis (congenital adrenal hyperplasia or SmithLemli-Opitz)</span></li>
                                    <li><span>Disorders of metal metabolism (Menkes syndrome, neonatal hemochromatosis)</span></li>
                                </ul>
