                            <div class="half-spaced-content">
                                <div>
                                    <p>Inborn Errors of Metabolism (IEM) comprise a group of disorders in which a genetic defect causes a clinically significant block in a metabolic pathway resulting either in accumulation of substrate behind the block or deficiency of the product. Most of the metabolic disorders are genetic conditions. While metabolic disorders are rare individually, collectively they affect 1-3% of the world population; 1 in 1000 infants are known to inherit a metabolic disorder, which therefore creates a health burden in the society. The onset of symptoms can be sudden or progress slowly. Symptoms may be brought on by foods, medications, dehydration, minor illnesses, or other factors. Symptoms appear within a few weeks after birth in several metabolic conditions. Other inherited metabolic disorders might take years to develop. Many individual disorders have clinical presentations that initially seem very similar to one another. All IEMs are all genetically transmitted typically in an autosomal recessive or X-linked recessive fashion.</p>
