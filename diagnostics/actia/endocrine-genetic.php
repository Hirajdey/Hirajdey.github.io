                            <div class="content-block common-endocrine-disorders">
                                <div class="text-content">
                                    <h3 class="gray italic">The common endocrine disorders can now rely on Genetic tests</h3>
                                    <div class="spacer-30"></div>
                                    <h3 class="bulleted ordered">Congenital hypothyroidism</h3>
                                    <ul class="green-bullet bb">
                                        <li><span>Partial or complete loss of function of the thyroid gland</span></li>
                                        <li><span>Thyroid dysgenesis is seen in 80 to 85 percent of cases</span></li>
                                        <li><span>Dyshormonogenesis in remainder of the cases</span></li>
                                        <li><span>If untreated, can lead to intellectual disability and slow growth</span></li>
                                        <li><span>Inheritance could be autosomal recessive or dominant</span></li>
                                        <li><span>The disorder can be syndromic (Pendred syndrome and brain-lung-thyroid syndrome) or non-Syndromic</span></li>
                                    </ul>
                                    <h3 class="bulleted ordered">Obesity:</h3>
                                    <p>The involvement of genetic factors is estimated to be 40-70% in obesity</p>
                                    <ul class="green-bullet bb">
                                        <li><span>Monogenic obesity is severe early-onset obesity, associated with endocrine disorders, mainly due to mutations in obesogenic or leptogenic genes (LEP, LEPR, POMC, PC1, MC4R)</span></li>
                                        <li><span>Syndromic obesity refers to obesity associated with other genetic syndromes like Prader-Willi and Bardet-Biedl. But more than 100 syndromes are now</span></li>
                                        <li><span>MedGenome offers a monogenic obesity panel covering majority of single genes as well as syndromic obesity genes.</span></li>
                                    </ul>
                                    <h3 class="bulleted ordered">Diabetes:</h3>
                                    <p>Diabetes mellitus is an etiologically heterogeneous disorder with Type 1 and 2 diabetes mellitus being multifactorial and polygenic. Neonatal Diabetes mellitus and Maturity-onset diabetes of the young (MODY) are the two main forms of monogenic diabetes.</p>

                                    <h3 class="bulleted ordered alphabetic">Maturity-onset diabetes of the young (MODY)</h3>
                                    <ul class="green-bullet bb">
                                        <li><span>Is a monogenic form of diabetes characterized by a primary defect in pancreatic ß-cell function</span></li>
                                        <li><span>Has an early onset, and an autosomal dominant mode of inheritance</span></li>
                                        <li><span>Though MODY can occur at any age, it is more likely to affect adolescents and young adults</span></li>
                                        <li><span>Often misdiagnosed as type 1 or type 2 Diabetes mellitus</span></li>
                                        <li><span>Till date, at least 13 MODY subtypes with distinct genetic aetiologies have been identified</span></li>
                                        <li><span>A correct genetic diagnosis is important as it often leads to a personalized treatment for the patient and enables predictive genetic testing for their asymptomatic relatives</span></li>
                                        <li><span>Next-generation sequencing provides an efficient method for screening mutations in this form of Diabetes as well as identifying new  MODY genes</span></li>
                                    </ul>
                                    <h3 class="bulleted ordered alphabetic">Neonatal/congenital:</h3>
                                    <p>Diabetes occurring under 6 months of age is predominantly monogenic</p>
                                    <ul class="green-bullet">
                                        <li><span>The fraction of cases without a known cause is diminishing rapidly as new genes are discovered</span></li>
                                        <li><span>The phenotype encapsulates numerous subtypes, wherein most aetiologies involve a severe disruption in ß-cell function</span></li>
                                        <li><span>Can be permanent and require lifelong treatment, or may be transient, in which case the diabetes may spontaneously remit (or be so 	    mild as not to require treatment), but will often relapse, usually during adolescence</span></li>
                                    </ul>
                                    <p>Genetic testing can diagnose most forms of monogenic diabetes. If genetic testing is not performed, people with monogenic diabetes may appear to have one of the polygenic forms of Diabetes. A correct diagnosis allows the proper treatment to be selected and it should lead to better glucose control and improved health in the long term. Testing of other family members may also be indicated to determine whether they are at a higher risk for Diabetes.</p>
                                    <div class="bb"></div>
                                    <h3 class="bulleted ordered">Diabetes:</h3>
                                    <div class="reset-subsection"></div>
                                    <h3 class="bulleted ordered alphabetic">Congenital Adrenal Hyperplasia (CAH)</h3>
                                    <ul class="green-bullet bb">
                                        <li><span>Most common DSD</span></li>
                                        <li><span>CAH are a family of autosomal recessive disorders involving impaired synthesis of cortisol from cholesterol by the adrenal cortex</span></li>
                                        <li><span>Among all CAH disorders, 21-hydroxylase deficiency (21-OHD) is the most common cause</span></li>
                                        <li><span>The diagnosis of classic 21-OHD CAH is established in new-borns with characteristic clinical features, elevated serum 17-OHP, and elevated adrenal androgens</span></li>
                                        <li><span>Sequence analysis of CYP21A2 is performed  first followed by gene-targeted deletion/ duplication analysis if only one or no 			    pathogenic variant is found</span></li>
                                        <li><span>Almost 70%-80% of patients can be detected by sequence analysis while 20%-30% may require gene-targeted deletion/duplication 	    analysis by MLPA</span></li>
                                        <li><span>To avoid diagnostic errors, studying both parents as well as the proband is recommended to confirm the pathogenic variants and to determine if they are in cis-configuration or trans-configuration</span></li>
                                    </ul>
                                    <h3 class="bulleted ordered alphabetic">Androgen receptor deficiency second common DSD after CAH</h3>
                                    <ul class="green-bullet">
                                        <li><span>The gene encoding androgen receptor (AR) is located on the X chromosome</span></li>
                                        <li><span>Mutations in AR gene cause androgen insensitivity syndrome (AIS), formerly known as the testicular feminization syndrome (TFM)</span></li>
                                        <li><span>The androgen insensitivity syndrome is an X-linked recessive disorder in which affected males have female external genitalia, female breast development, blind vagina, absent uterus and female adnexa, and abdominal or inguinal testes, despite a normal male 46,XY karyotype</span></li>
                                        <li><span>Partial androgen insensitivity results in hypospadias and micropenis with gynecomastia (Reifenstein syndrome)</span></li>
                                        <li><span>Sequence analysis by NGS can detect almost 95%-97% of the patients with AIS</span></li>
                                        <li><span>Gene targeted deletion/duplication analysis to detect multi-exon or whole-gene deletions or duplications may be considered if a pathogenic variant in AR is not identified by sequence analysis (3-5% of the cases)</span></li>
                                        <li><span>Medgenome offers MLPA technique for testing Androgen receptor (AR) deletion/ duplication analysis and NSG for AR NGS analysis</span></li>
                                    </ul>
                                    <p>Kallmann Syndrome:</p>
                                    <ul class="green-bullet">
                                        <li><span>Characterized by delayed or absent puberty and an impaired sense of smell (hyposmia) or completely absent (anosmia)</span></li>
                                        <li><span>This feature distinguishes Kallmann syndrome from most other forms of hypogonadotropic hypogonadism, which do not affect the sense of smell</span></li>
                                        <li><span>Many people with Kallmann syndrome are not aware that they are unable to detect odors discovered through testing</span></li>
                                        <li><span>More than 20 genes have been associated with Kallmann syndrome, and mutations in the ANOS1, CHD7, FGF8, FGFR1, PROK2 or PROKR2 genes are the most common</span></li>
                                        <li><span>In some cases, affected individuals have mutations in more than one of these genes. Medgenome offers a gene panel analysis for all the involved genes using NGS</span></li>
                                    </ul>
                                    <div class="bb"></div>
                                    <div class="reset-subsection"></div>
                                    <h3 class="bulleted ordered">Familial lipid disorders:</h3>
                                    <ul class="green-bullet">
                                        <li><span>Patients with lipid disorders have trouble maintaining normal levels of body fats, and these disorders can be found in several conditions that require special management, including metabolic syndrome, polycystic ovary syndrome (PCOS), and obesity. Special diets, exercise, and medications may be prescribed to manage hyperlipidemia and other lipid disorders</span></li>
                                        <li><span>A familial lipid disorder is a condition that causes very high levels of cholesterol. This condition can lead to early onset coronary artery disease (CAD).</span></li>
                                    </ul>
                                    <p>There are different types of lipid disorders. They include:</p>
                                    <ol class="green-numbered">
                                        <li>Familial combined hyperlipidemia (FCHL)</li>
                                        <li>Familial defective apolipoprotein B-100</li>
                                        <li>Familial dysbetalipoproteinemia (type 3 hyperlipoproteinemia)</li>
                                        <li>Familial hypertriglyceridemia</li>
                                        <li>Heterozygous familial hypercholesterolemia</li>
                                    </ol>
                                    <div class="spacer-20"></div>
                                    <p>Primary causes of dyslipidemias involve gene mutations that cause the body to produce too much LDL cholesterol or triglycerides, or the failure to remove these substances. Some causes involve underproduction or excessive removal of HDL cholesterol. Primary causes tend to be inherited and thus run in families. Multi-gene panel involving all the associated genes in dyslipidiema are available for genetic testing. MLPA may be required for testing deletion/ duplication in certain genes. For ex. sequence analysis of LDLR is performed  first followed by LDLR deletion/duplication analysis if no pathogenic variant is found in the case of Familial Hypercholesterolemia.</p>
