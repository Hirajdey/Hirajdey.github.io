                        <div id="toggle1" class="content-block genetic-renal-disorders">
                            <div class="text-content">
                                <p>The prevalence of renal diseases is rising and reaching 5-15% of the adult population. Studies indicate that renal diseases are influenced not only by environmental but also by genetic factors <sup>[1]</sup>.</p>
                                <div class="spacer-20"></div>
                                <p>An estimated 20% of cases of CKD are thought to be due to genetic forms of renal disease, as evidenced by familial clustering and differing prevalence rates across ethnic groups <sup>[2]</sup>.</p>
                                <div class="spacer-20"></div>
                                <p>The prevalence of renal disease in children, including those with a GFR of <30 ml/min/1.73 2 superscript to ESRD, ranges from 21 to 55 per million age represtentative population. In countries with high rates of consanguinity and potentially greater risks of autosomal recessive disease, approximately one fifth to one third of paediatric patients with ESRD are reported as having hereditary renal disease <sup>[3]</sup>.</p>
                                <p class="light"><sup>[1]</sup> Gluba-Brzózka A, Franczyk B, Olszewski R, et al. Int J Mol Sci. 2017 Jun 10;18(6).</p>
                                <p class="light"><sup>[2]</sup> Mallett A, Patel C, Salisbury A, et al. Orphanet Journal of Rare Diseases. 2014;9:98.</p>
                                <p class="light"><sup>[3]</sup> Fletcher, J., McDonald, S., Alexander, S.I. et al. Pediatr Nephrol (2013) 28: 251. [*]Hildebrandt F: Genetic kidney diseases. Lancet. 2010, 375 (9722): 1287-1295.</p>
                            </div>

                            <div class="content-block prevalence-of-renal-diseases">
                                <div class="text-content">
                                    <h3>Prevalence of Renal Diseases</h3>
                                    <div class="spacer-20"></div>
                                    <div class="wide-lightgreen-block">
                                        <div class="image-content flex">
                                            <div class="inner-content"><img src="img/prevalence-of-renal-diseases-illustration.png" alt="Prevalence of Renal Diseases"></div>
