                                    </div>
                                    <h3 class="lighter-green">Renal Cystic & Interstitial Diseases</h3>
                                    <p>Defects of the cilial system are strongly related to the development of cystic kidney disease at any stage of life. The cystic kidney diseases are also known as renal cystic ciliopathies. Some of these are:</p>
                                    <ul class="green-bullet bb">
                                        <li><span>Bartter syndrome</span></li>
                                        <li><span>Gitelman syndrome</span></li>
                                        <li><span>Pseudohypoaldosteronism</span></li>
                                        <li><span>dRTAv</span></li>
                                    </ul>
                                    <h3 class="lighter-green">Renal Tubular Diseases</h3>
                                    <p>Renal tubular function governs reabsorption of water and solutes from the glomerular filtrate. In renal tubulopathies the primary genetic defect causes loss of function of a specific renal transport protein or signalling molecule. The following are classified under genetic renal tubular disorders.</p>
                                    <ul class="green-bullet bb">
                                        <li><span>ADPKD</span></li>
                                        <li><span>ARPKD</span></li>
                                        <li><span>ADTKD</span></li>
                                        <li><span>Nephronophthisis</span></li>
                                    </ul>
                                    <h3 class="lighter-green">Renal Glomerular Disorders</h3>
                                    <p>Glomeruli are the tiny units within the kidney where blood is cleaned. Glomerulonephritis (inflammation of the membrane tissue in the kidney that serves as a filter) and glomerulosclerosis (hardening of the tiny blood vessels within the kidney) are the two major categories of glomerular diseases. Some genetic glomerular diseases are:</p>
                                    <ul class="green-bullet bb">
                                        <li><span>SRNS</span></li>
                                        <li><span>Familial atypical HUS</span></li>
                                        <li><span>Alport syndrome</span></li>
                                        <li><span>Fibronectin glomerulopathy</span></li>
                                    </ul>
                                    <h3 class="lighter-green">Nephrolithiasis</h3>
                                    <p>The heritability of renal stone formation (nephrolithiasis) has long been recognized. And the advent of the genomic era has greatly increased the potential to define its underlying genetic defects. These defects which lead to stone formation have been valuable in ddefining renal pathophysiology in these diseases.</p>
                                    <ul class="green-bullet bb">
                                        <li><span>Primary Hyperoxaluria</span></li>
                                        <li><span>Cystinuria</span></li>
                                        <li><span>Hereditary Xanthinuria</span></li>
                                        <li><span>Dent disease</span></li>
                                    </ul>
                                    <h3 class="lighter-green">Congenital abnormalities of the kidney and urinary tract (CAKUT)</h3>
                                    <p>CAKUT account for approximately 50% of children with end-stage kidney disease. They occur in about 3 to 6 per 1,000 live births and constitute 20–30% of all anomalies identified in the neonatal period. They may present as an isolated feature or as part of clinical syndromes. Single-gene mutations in many different genes may cause a wide phenotypic spectrum of CAKUT and these are listed below</p>
                                    <ul class="green-bullet bb">
                                        <li><span>Renal agenesis (RA)</span></li>
                                        <li><span>Renal hypodysplasia (RHD)</span></li>
                                        <li><span>Multicystic renal dysplasia (MRD)</span></li>
                                        <li><span>Vesicoureteral reflux (VUR 2)</span></li>
                                    </ul>
                                    <h3 class="lighter-green">How genomics has helped patients with Genetic Renal Disorders</h3>

                                    <h4>Highly penetrant mutations cause a wide range of renal phenotypes including:</h4>
                                    <ul class="green-bullet">
                                        <li><span>Disease of renal growth (eg. polycystic kidney disease)</span></li>
                                        <li><span>Diseases of abnormal glomerular function (eg. congenital nephrotic syndrome)</span></li>
                                        <li><span>Abnormalities of blood pressure and electrolyte homeostasis (eg. Bartter syndrome)</span></li>
                                    </ul>
                                    <h4>Correct diagnosis of GRD impacts</h4>
                                    <ul class="green-bullet bb">
                                        <li><span>Specific investigations</span></li>
                                        <li><span>Affect on the treatment</span></li>
                                        <li><span>Consequences on the whole family such as identifying the affected subjects and taking decisions regarding specific therapy</span></li>
                                    </ul>
                                    <h3 class="gray italic">Examples:</h3>
                                    <div class="spacer-10"></div>
                                    <h4>1. Atypical Haemolytic Uraemic Syndrome (aHUS)</h4>
                                    <p>Until recently a HUS was an untreatable disease. Understanding the genetics of a HUS has revealed that most individuals carry mutations in complement genes which results in over activation of the complement system. This resulted in the successful clinical trial of the complement inhibitor Eculizumab. Proven mutations in complement genes predicts response to Eculizumab, while mutations in the noncomplement gene DGKE, is a marker of non-response in a subgroup of patients.</p>
                                    <h4>2. The Role of UMOD in Hypertension and Chronic Kidney Disease</h4>
                                    <p>Mutations in UMOD, which encodes uromodulin, are associated with Medullary Cystic Kidney Disease Type 2, a rare dominantly inherited cause of CKD. Recent GWAS studies have identified susceptibility variants for chronic kidney disease and hypertension in UMOD. Risk variants in UMOD directly increase UMOD expression, leading to salt-sensitive hypertension, secondary to activation of the renal sodium- potassium-chloride cotransporter NKCC2. Uromodulin may be a novel therapeutic target to control blood pressure and preserve renal function.</p>
                                    <div class="spacer-20"></div>
                                    <p class="light">Source: Mallett A, et al. Genomics in the renal clinic - translating nephrogenetics for clinical practice. Hum Gen. 2015 Jun 24;9:13.</p>
